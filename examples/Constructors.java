package examples;

/**
 * Constructors are used to make an object and initialize it, thus giving the object an identity of real object
 * <p></p>
 * Constructors don't have return values
 * <p></p>
 * If no constructors are made then a default constructor is made by the java compiler
 * <p></p>
 * Any level of access modifiers can be used to create a constructor
 */
public class Constructors {
    Constructors() {
        System.out.println("Hello, I am initialized.");
    }
}
